package start;

public class Berry{
    private float restoreValue;
    private float restoreWeight;
    
    public Berry(){
        this.restoreValue = 10;
        this.restoreWeight = 5;
    }

    public float getRestoreValue(){
        return this.restoreValue;
    }

    public float getRestoreWeight(){
        return this.restoreWeight;
    }
}